    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if (nameTxt.getText().isEmpty() || usernameTxt.getText().isEmpty() || passwordTxt.getText().isEmpty() || numberTxt.getText().isEmpty() || emailTxt.getText().isEmpty() )
        {
            JOptionPane.showMessageDialog(this,"Missing Information. Please re-try.");
        }else if (validateName(nameTxt.getText()) == false)
        {
            lblName.setText("Invalid Name.");
            System.out.println("invalid last name" + nameTxt.getText());
        }else if (validateEmail(emailTxt.getText().toString()) == false )
        {
            lblEmail.setText("Invalid Email.");
            System.out.println("invalid email");
        }else if (validatePhone(numberTxt.getText()) == false )
        {

            lblNumber.setText("Invalid Phone Number.");
            System.out.println("invalid number");
        }else if (validateUsername(usernameTxt.getText().toString()) == false )
        {
            lblUsername.setText("Invalid Username.");
            JOptionPane.showMessageDialog(this,"Invalid Username. Please make sure your username starts with a letter and consists of 6 - 30 characters excluding special characters.");
            System.out.println("invalid username");
        }else if (validateUsername(passwordTxt.getText()) == false )
        {
            JOptionPane.showMessageDialog(this,"Invalid Password. Please make sure your pasword starts with a letter and consists of 6 - 30 characters excluding special characters.");
            System.out.println("invalid password");
        } 
        else{
            try{
                BufferedReader reader = Files.newBufferedReader(filePath);
                String line;
                int found;
                String username = lblUser.getText();
                while ((line = reader.readLine()) != null) {
                    String[] fields = line.split("[,]");
                    System.out.println(lblUser + fields[1]);
                    if (username.equals(fields[1])){
                        found = 1;
                        String name = nameTxt.getText();
                        String usern = usernameTxt.getText();
                        String email = emailTxt.getText();
                        String number = numberTxt.getText();
                        String password = passwordTxt.getText();
                        String u = lblUser.getText();
                        
                        editRecord(name, usern, email, number, password, found, u);

                        if (found == 0)
                        {
                            JOptionPane.showMessageDialog(rootPane, "Record not found.");
                        }
                        if (found == 1)
                        {
                            JOptionPane.showMessageDialog(rootPane, "Record Updated Successfully");

                        }

                    }
                }
            }  catch (Exception ex) {
                ex.printStackTrace();
            }

        }
    }//GEN-LAST:event_btnEditActionPerformed

