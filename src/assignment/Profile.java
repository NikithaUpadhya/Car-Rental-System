 public static void editRecord (String name, String usern, String email, String number, String password, int found, String u){
    try
        {
            System.out.println("u equals:" + u);
            Path old = Paths.get(".", "CustomerData.txt");
            
            Path tempFile = Files.createTempFile(old.getParent(), "PendingTemp", ".txt");
            try (BufferedReader reader = Files.newBufferedReader(old);
                BufferedWriter writer = Files.newBufferedWriter(tempFile))
            {
               
                String line123;
                // copy everything until the id is found
                while ((line123 = reader.readLine()) != null)
                {
                    String[] fields = line123.split("[,]");
                    //System.out.println(fields[1]);
                    //System.out.println("u equals:" + u);
                    if (u.equals(fields[1]))
                    {
                        found = 1;
                        
                        fields[0] = name;
                        fields[1] = usern;
                        fields[2] = email;
                        fields[3] = number;
                        fields[4] = password;
                                           }
                    writer.write(String.join(",", fields));
                    writer.newLine();
                    found = 1;
                }             
            }
            // copy new file & delete temporary file
            Files.copy(tempFile, old, StandardCopyOption.REPLACE_EXISTING);
            Files.delete(tempFile);
           
        }catch (IOException ex){} 
    }
 
